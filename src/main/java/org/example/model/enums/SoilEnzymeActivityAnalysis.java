package org.example.model.enums;

// 土壤酶活性分析类型枚举。
// 该枚举表示不同类型的土壤酶活性分析，用于标识土壤分析中不同的酶活性。
public enum SoilEnzymeActivityAnalysis
{
    AcidPhosphatase,     // AcidPhosphatase - 表示土壤中的酸性磷酸酶活性。
    AcidProtease, // AcidProtease - 表示土壤中的酸性蛋白酶活性。
    Urease,  // Urease - 表示土壤中的尿素酶活性。
    NitrateReductase, // NitrateReductase - 表示土壤中的硝酸还原酶活性。
    MicrobialCommunityStructure,  // MicrobialCommunityStructure - 表示土壤中的微生物群落结构分析。
    MicrobialCompositionAnalysis, // MicrobialCompositionAnalysis - 表示土壤中的微生物组成分析。
    SpeciesDifferentiationAnalysis, // SpeciesDifferentiationAnalysis - 表示土壤中的物种区分分析。
    MicrobialCorrelationClustering,  // MicrobialCorrelationClustering - 表示土壤中的微生物关联聚类分析。
    FunctionalPrediction16S; // FunctionalPrediction16S - 表示16S基因分析中的功能预测。
}
